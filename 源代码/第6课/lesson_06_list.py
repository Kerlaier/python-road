# -*- coding: utf-8 -*-
import sys
sys.stdout.reconfigure(encoding='utf-8')
"""
============================================================
  Python 从入门到精通 —— 第6课：列表 list
  实战场景：购物车管理、成绩排名、待办清单
============================================================
"""

# ============================================================
# 一、什么是列表？—— 能装多个东西的容器
# ============================================================
print("[场景1] 购物车")
print("-" * 30)

# 语法：用 [] 创建，里面用逗号隔开
cart = ["苹果", "香蕉", "牛奶"]    # 字符串列表
scores = [95, 88, 73, 90]          # 数字列表
mixed = ["小明", 20, True, 95.5]   # 混合类型（可以但不常用）

print(f"购物车: {cart}")
print(f"成绩单: {scores}")
print(f"有几样东西: {len(cart)}件")  # len() 查元素个数

# ============================================================
# 二、索引和切片 —— 和字符串一样！
# ============================================================
print("\n[场景2] 查成绩")
print("-" * 30)

scores = [95, 88, 73, 90]

# 索引从 0 开始，-1 是最后一个
print(f"scores[0]  = {scores[0]}       # 第一个成绩")    # 95
print(f"scores[-1] = {scores[-1]}      # 最后一个成绩")   # 90

# 切片：和字符串一模一样！
print(f"scores[1:3] = {scores[1:3]}      # 索引1到2（不含3）")  # [88, 73]
print(f"scores[:2]  = {scores[:2]}       # 前两个")            # [95, 88]

# ============================================================
# 三、增加元素 —— append / insert
# ============================================================
print("\n[场景3] 往购物车加东西")
print("-" * 30)

cart = ["苹果", "香蕉"]
print(f"初始购物车: {cart}")

cart.append("牛奶")              # append：加到最后面
print(f"append后: {cart}")       # ['苹果', '香蕉', '牛奶']

cart.insert(1, "草莓")           # insert(位置, 内容)：插到指定位置
print(f"在1号位插入: {cart}")    # ['苹果', '草莓', '香蕉', '牛奶']

# ============================================================
# 四、删除元素 —— remove / pop
# ============================================================
print("\n[场景4] 从购物车删东西")
print("-" * 30)

cart = ["苹果", "香蕉", "牛奶", "面包"]
print(f"当前购物车: {cart}")

cart.remove("香蕉")              # remove(值)：按内容删除
print(f"删掉香蕉后: {cart}")     # ['苹果', '牛奶', '面包']

popped = cart.pop()              # pop()：弹掉最后一个，返回它
print(f"pop出: {popped}")        # 面包
print(f"pop后: {cart}")          # ['苹果', '牛奶']

popped2 = cart.pop(0)            # pop(索引)：弹掉指定位置
print(f"pop第0个: {popped2}")    # 苹果
print(f"pop后: {cart}")          # ['牛奶']

# ============================================================
# 五、修改元素
# ============================================================
print("\n[场景5] 修改成绩")
print("-" * 30)

scores = [95, 88, 73, 90]
print(f"原成绩: {scores}")

scores[2] = 80                   # 把索引2（第3个）改成80
print(f"修改后: {scores}")       # [95, 88, 80, 90]

# ============================================================
# 六、常用操作 —— 排序、查找、遍历
# ============================================================
print("\n[场景6] 成绩排名")
print("-" * 30)

scores = [95, 88, 73, 90, 85]
print(f"原始: {scores}")

scores.sort()                    # sort()：从小到大排序
print(f"排序后: {scores}")       # [73, 85, 88, 90, 95]

scores.sort(reverse=True)        # sort(reverse=True)：从大到小
print(f"倒序: {scores}")         # [95, 90, 88, 85, 73]

# in 运算符：检查某个东西在不在列表里
print(f"\n90分在成绩单里吗？{90 in scores}")    # True
print(f"100分在成绩单里吗？{100 in scores}")    # False

# for 循环遍历
print("\n逐个打印成绩:")
for s in scores:
    print(f"  {s}分")

# ============================================================
# 七、实战：待办清单
# ============================================================
print("\n[场景7] 待办清单管理器")
print("-" * 30)

todos = ["写作业", "跑步"]                 # 初始清单
print(f"待办: {todos}")

todos.append("买菜")                       # 新增事项
print(f"增加后: {todos}")

todos.remove("跑步")                       # 完成"跑步"，删掉
print(f"完成后: {todos}")

print(f"\n共{len(todos)}件事待办:")               # len() 查个数           # len() 也一样
for i, task in enumerate(todos, 1):        # enumerate 同时拿索引和值
    print(f"  {i}. {task}")               # 编号从1开始

# ============================================================
# 本课小结
# ============================================================
print("\n" + "=" * 50)
print("[总结] 列表操作速查")
print("=" * 50)
