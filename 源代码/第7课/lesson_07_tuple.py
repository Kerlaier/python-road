# ============================================================
# 一、什么是元组？—— 不可修改的列表
# ============================================================
print("[场景1] GPS 坐标——一旦记录就不能改")
print("-" * 30)

# 语法：用 () 创建，和列表基本一样
point = (100, 200)           # 一个坐标点 (x, y)
person = ("小明", 20, "男")   # 一个人的信息

print(f"坐标点: {point}")
print(f"个人信息: {person}")

# 和列表的区别：元组不能改！
# point[0] = 150    #  ← 这会报错！注释掉给你看

# ============================================================
# 二、为什么需要元组？—— 场景决定一切
# ============================================================
print("\n[场景2] 什么时候用列表，什么时候用元组？")
print("-" * 30)

# 用列表：东西会变
shopping_cart = ["牛奶", "面包"]   # 随时增减, 用列表 ✅
shopping_cart.append("鸡蛋")       # 加东西, 没问题
print(f"购物车（列表）: {shopping_cart}")

# 用元组：东西固定不变
rgb_red = (255, 0, 0)             # 红色RGB值，不会变, 用元组 ✅
weekdays = ("周一", "周二", "周三", "周四", "周五")
print(f"红色RGB（元组）: {rgb_red}")
print(f"工作日（元组）: {weekdays}")

# 口诀：能变的用列表 []，不能变的用元组 ()

# ============================================================
# 三、索引和切片 —— 和列表一模一样
# ============================================================
print("\n[场景3] 读取坐标信息")
print("-" * 30)

point = (100, 200, 50)    # (x, y, z) 三维坐标

print(f"x坐标: {point[0]}")      # 100  — 和列表一样的索引
print(f"y坐标: {point[1]}")      # 200
print(f"z坐标: {point[2]}")      # 50
print(f"最后一位: {point[-1]}")   # 50  — 负数索引也一样

# 切片也完全一样
rgb = (255, 128, 0)
print(f"前两个值: {rgb[:2]}")     # (255, 128)  — 含头不含尾

# ============================================================
# 四、元组的"拆包"—— 一次性把值分给多个变量
# ============================================================
print("\n[场景4] 餐厅订单解析——一行拆出多个信息")
print("-" * 30)

order = ("红烧肉", 2, 38.0)      # (菜名, 份数, 单价)

# 拆包：一行把三个值分开
name, count, price = order

print(f"菜名: {name}")
print(f"份数: {count}")
print(f"单价: {price}")
print(f"总价: {count * price}元")   # 2 × 38 = 76

# 不用拆包也能这么写，但拆包更优雅：
# name = order[0]
# count = order[1]
# price = order[2]

# ============================================================
# 五、遍历元组 —— 和列表一样
# ============================================================
print("\n[场景5] 打印所有工作日")
print("-" * 30)

weekdays = ("周一", "周二", "周三", "周四", "周五")

for day in weekdays:
    print(f"  {day} 要上班")

# enumerate 也一样能用
for i, day in enumerate(weekdays, 1):
    print(f"  第{i}天: {day}")

# ============================================================
# 六、元组的常用操作
# ============================================================
print("\n[场景6] 成绩单查询")
print("-" * 30)

scores = (95, 88, 73, 90, 85)

print(f"共{len(scores)}个成绩")          # len() — 查个数
print(f"最高分: {max(scores)}")          # max() — 最大值
print(f"最低分: {min(scores)}")          # min() — 最小值
print(f"总分: {sum(scores)}")            # sum() — 求和
print(f"88在成绩单里吗？{88 in scores}")  # in — 判断存在
print(f"66在成绩单里吗？{66 in scores}")

# ============================================================
# 七、特殊情况：单元素元组
# ============================================================
print("\n[注意] 只有一个元素的元组要加逗号")
print("-" * 30)

not_tuple = (5)           # ❌ 这是数字5，不是元组！
yes_tuple = (5,)          # ✅ 加逗号才是元组

print(f"(5)   的类型: {type(not_tuple)}")   # <class 'int'>
print(f"(5,)  的类型: {type(yes_tuple)}")   # <class 'tuple'>

# ============================================================
# 八、实战：学生成绩记录（元组版）
# ============================================================
print("\n[场景7] 学生成绩记录系统")
print("-" * 30)

# 每个学生是一条元组：(姓名, 语文, 数学, 英语)
students = [
    ("小明", 90, 85, 88),
    ("小红", 78, 92, 80),
    ("小刚", 85, 76, 91),
]

for name, chinese, math, english in students:
    total = chinese + math + english
    avg = total / 3
    print(f"{name}: 总分{total}, 平均{avg:.1f}")

# ============================================================
# 本课小结
# ============================================================
